#include "qk_adc.h"


u16 ADC_Results[4];

/****************************************************************

	函数功能：	ADC的初始化
	输入参数：	无
	返回：		无
	更新日期:	2014-4-2
	备注：		无

*****************************************************************/
void AdcInit(void)
{
	ADC_InitTypeDef ADC_InitStructure;
	GPIO_InitTypeDef GPIO_InitStructure;
	DMA_InitTypeDef DMA_InitStructure; 

	//使能ADC1,ADC2
	RCC_APB2PeriphClockCmd(RCC_APB2Periph_ADC1 | RCC_APB2Periph_ADC2,ENABLE);
	RCC_ADCCLKConfig(RCC_PCLK2_Div8);
	//使能GPIOC
	RCC_APB2PeriphClockCmd(RCC_APB2Periph_GPIOC,ENABLE);
	//使能DMA
	RCC_AHBPeriphClockCmd(RCC_AHBPeriph_DMA1,ENABLE);

	//配置PC0(channel 10),PC1(channel 11),PC2(channel 12),PC3(channel 13),PC4(channel 14)设置为ADC的输入通道
	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_0 | GPIO_Pin_1 | GPIO_Pin_2 | GPIO_Pin_4; 
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_2MHz;
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_AIN;
	GPIO_Init(GPIOC, &GPIO_InitStructure);
	
	//DMA配置
	DMA_DeInit(DMA1_Channel1);
	DMA_InitStructure.DMA_PeripheralBaseAddr = ADC1_DR_Address;							//外设地址
	DMA_InitStructure.DMA_MemoryBaseAddr = (u32)&ADC_Results;							//内存地址
	DMA_InitStructure.DMA_DIR = DMA_DIR_PeripheralSRC;									//dma传输方向单向
	DMA_InitStructure.DMA_BufferSize = 4;												//设置DMA在传输时缓冲区的长度
	DMA_InitStructure.DMA_PeripheralInc = DMA_PeripheralInc_Disable;					//设置DMA的外设递增模式，一个外设
	DMA_InitStructure.DMA_MemoryInc = DMA_MemoryInc_Enable;								//设置DMA的内存递增模式，
	DMA_InitStructure.DMA_PeripheralDataSize = DMA_PeripheralDataSize_HalfWord;			//外设数据字长
	DMA_InitStructure.DMA_MemoryDataSize = DMA_MemoryDataSize_HalfWord;					//内存数据字长
	DMA_InitStructure.DMA_Mode = DMA_Mode_Circular;										//设置DMA的传输模式：连续不断的循环模式
	DMA_InitStructure.DMA_Priority = DMA_Priority_High;									//设置DMA的优先级别
	DMA_InitStructure.DMA_M2M = DMA_M2M_Disable;										//设置DMA的2个memory中的变量互相访问
	DMA_Init(DMA1_Channel1, &DMA_InitStructure);
	
	DMA_Cmd(DMA1_Channel1, ENABLE);														//使能DMA

	//ADC1配置
	ADC_InitStructure.ADC_Mode = ADC_Mode_Independent;									//独立工作模式
	ADC_InitStructure.ADC_ScanConvMode = ENABLE;										//扫描方式
	ADC_InitStructure.ADC_ContinuousConvMode = DISABLE;									//连续转换
	ADC_InitStructure.ADC_ExternalTrigConv = ADC_ExternalTrigConv_None;					//外部触发禁止
	ADC_InitStructure.ADC_DataAlign = ADC_DataAlign_Right;								//数据右对齐
	ADC_InitStructure.ADC_NbrOfChannel = 4;												//用于转换的通道数
	ADC_Init(ADC1, &ADC_InitStructure);
	
	//规则模式通道配置
	ADC_RegularChannelConfig(ADC1, ADC_Channel_10, 1, ADC_SampleTime_7Cycles5);
	ADC_RegularChannelConfig(ADC1, ADC_Channel_11, 2, ADC_SampleTime_7Cycles5);  
	ADC_RegularChannelConfig(ADC1, ADC_Channel_12, 3, ADC_SampleTime_7Cycles5);
	ADC_RegularChannelConfig(ADC1, ADC_Channel_14, 4, ADC_SampleTime_7Cycles5);
	
	/* Enable ADC1 DMA [使能ADC1 DMA]*/
  	ADC_DMACmd(ADC1, ENABLE);
  																		
	/* Enable ADC1 */
	ADC_Cmd(ADC1, ENABLE);//

	/* Enable ADC1 reset calibaration register */   
	ADC_ResetCalibration(ADC1);
	/* Check the end of ADC1 reset calibration register */
	while(ADC_GetResetCalibrationStatus(ADC1));
	
	/* Start ADC1 calibaration */
	ADC_StartCalibration(ADC1);
	/* Check the end of ADC1 calibration */
	while(ADC_GetCalibrationStatus(ADC1));
	
	ADC_SoftwareStartConvCmd(ADC1,ENABLE);

}

